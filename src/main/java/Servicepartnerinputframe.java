    private void btgoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btgoActionPerformed
        String sp_type=textservice.getText().trim();
        String spap_type=textapp.getText().trim();
        String spap_brand=textbrand.getText().trim();
        if(sp_type.isEmpty()){
           lblmess.setText("Please Fill Required Fields");
            return;
        }
        try{
            Class.forName("com.mysql.jdbc.Driver");
	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            PreparedStatement ps= con.prepareStatement("Select * from partner where sp_type=? and spap_type=? and spap_brand=?");
            ps.setString(1,sp_type);
            ps.setString(2,spap_type);
            ps.setString(3,spap_brand);
            ResultSet rs=ps.executeQuery();
            if (rs.next()) {
                new Servicepartnerdetailframe(sp_type,spap_type,spap_brand).setVisible(true);
                this.dispose(); 
            
            } 
            else {
                lblmess.setText("No record found!");
            }
        }
        catch(Exception e){
              System.out.println(e);
        }
                                        
    }//GEN-LAST:event_btgoActionPerformed

