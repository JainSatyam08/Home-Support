    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
        try{
            Random r1=new Random();
            int a=r1.nextInt(1,9);
            int b=r1.nextInt(1,9);
            int c=r1.nextInt(1,9);
            int d=r1.nextInt(1,9);
	    String sp_id= "ISCSP"+a+""+b+""+c+""+d;
            String sp_pass= "";
            String sp_name= textname.getText().trim();
            String sp_cno=textcno.getText().trim();
            
            String sp_type=cmbtype.getSelectedItem().toString().trim();;
            int vs_charge=0;
            if(sp_type.equalsIgnoreCase("Electrician")){
                sp_pass= "spar"+c+""+d+""+"@@el";
                vs_charge=100;
            }
            if(sp_type.equalsIgnoreCase("Plumber")){
                sp_pass= "spar"+c+""+d+""+"@@pl";
                vs_charge=100;
            }
            if(sp_type.equalsIgnoreCase("Appliance Care")){
                sp_pass= "spar"+c+""+d+""+"@@ap";
                vs_charge=250;
                spap_type = cmbapp.getSelectedItem().toString().trim();
                spap_brand = cmbbrand.getSelectedItem().toString().trim();
            }
            if(sp_type.equalsIgnoreCase("Painter")){
                sp_pass= "spar"+c+""+d+""+"@@pt";
                vs_charge=300;
            }
            if(sp_type.equalsIgnoreCase("Carpenter")){
                sp_pass= "spar"+c+""+d+""+"@@cp";
                vs_charge=100;
            }
            if(sp_type.equalsIgnoreCase("SERVICE TYPE")){
                lblmess.setText("Select Partner Type"); 
                return;
            }
            if (sp_name.isEmpty() || sp_cno.isEmpty()) {
                lblmess.setText("‚ùå Please fill all the mandatory fields.");
                return;
            }
            
            Class.forName("com.mysql.jdbc.Driver");
	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            String qry="insert into partner(sp_id,sp_pass,sp_name,sp_cno,sp_type,spap_type,spap_brand,sp_status,tp_pending,vs_charge) values(?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement ps1= con.prepareStatement(qry);
	    ps1.setString(1,sp_id);
	    ps1.setString(2,sp_pass);
	    ps1.setString(3,sp_name);
	    ps1.setString(4,sp_cno);
	    ps1.setString(5,sp_type);
	    ps1.setString(6,spap_type);
	    ps1.setString(7,spap_brand);
	    ps1.setString(8,"Active");
            ps1.setInt(9,0);
            ps1.setInt(10,vs_charge);
            int a1=ps1.executeUpdate();
            if (a1==1){
                dispose();
                new Hireconfirmframe(sp_id,sp_pass,"Partner").setVisible(true);
            }
            else{
                lblmess.setText("ERROR!! Try Again");
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
             
    }//GEN-LAST:event_btaddActionPerformed

