    private void btassignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btassignActionPerformed
        String r_id=textr_id.getText().trim();
        String c_name=textcname.getText().trim();
        String sp_id=textsp_id.getText().trim();
        String up_date=textup_date.getText().trim();
        if (!up_date.matches("\\d{4}-\\d{2}-\\d{2}")) {
            lblmess.setText("‚ùå Please enter date in YYYY-MM-DD format.");
            return;
        }
        if (up_date.isEmpty()) {
            lblmess.setText("Please enter date");
            return;
        }
        if (r_id.isEmpty()) {
            lblmess.setText("Please enter request id");
            return;
        }
        if (c_name.isEmpty()) {
            lblmess.setText("Please enter name");
            return;
        }
        if (sp_id.isEmpty()) {
            lblmess.setText("Please enter SP id");
            return;
        }
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            String qry="update status set status=? where r_id=? and c_name=?";
            PreparedStatement ps=con.prepareStatement(qry);
            ps.setString(1,"Assinged");
            ps.setString(2,r_id);
            ps.setString(3,c_name);
            int a1=ps.executeUpdate();
            String qry1="update status set sp_id=? where r_id=? and c_name=?";
            PreparedStatement ps1=con.prepareStatement(qry1);
            ps1.setString(1,sp_id);
            ps1.setString(2,r_id);
            ps1.setString(3,c_name);
            int a2=ps1.executeUpdate();
            String qry2="update status set up_date=? where r_id=? and c_name=?";
            PreparedStatement ps2=con.prepareStatement(qry2);
            ps2.setString(1,up_date);
            ps2.setString(2,r_id);
            ps2.setString(3,c_name);
            int a3=ps2.executeUpdate();
            if (a1==1&& a2==1 && a3==1){
                lblmess.setText("SERVICE PARTNER ASSIGNED");
                textr_id.setText("");
                textcname.setText(" ");
                textsp_id.setText("");
                textup_date.setText("");
            
            }
            else{
                lblmess.setText(" SORRY!!! TRY AGAIN !!!!");
                textr_id.setText("");
                textcname.setText(" ");
                textsp_id.setText("");
                textup_date.setText("");
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btassignActionPerformed

