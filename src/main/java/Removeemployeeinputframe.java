    private void btgoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btgoActionPerformed
        String emp_id=textid.getText().trim();
        String name=textname.getText().trim();
        if(emp_id.isEmpty()){
            lblmess.setText("Please Enter Employee Id");
            return;
        }
        if(name.isEmpty()){
            lblmess.setText("Please Enter Employee Name");
            return;
        }
        try{
            Class.forName("com.mysql.jdbc.Driver");
	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            PreparedStatement ps= con.prepareStatement("Select * from employee where emp_id=? and emp_name=?");
            ps.setString(1,emp_id);
            ps.setString(2,name);
            ResultSet rs=ps.executeQuery();
            if (rs.next()) {
                new Removeemployeedetailframe(emp_id,name).setVisible(true);
                this.dispose(); 
            
            } 
            else {
                lblmess.setText("No record found!");
            }
        }
        catch(Exception e){
              System.out.println(e);
        }
    }//GEN-LAST:event_btgoActionPerformed

