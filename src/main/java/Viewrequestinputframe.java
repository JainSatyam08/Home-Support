    private void btgoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btgoActionPerformed
        String inputdate=textdate.getText().trim();
        if(inputdate.isEmpty()){
            lblmessage.setText("Please Enter Date");
            return;
        }
        if (!inputdate.matches("\\d{4}-\\d{2}-\\d{2}")) {
                lblmessage.setText("‚ùå Please enter date in YYYY-MM-DD format.");
                return;
        }        
        try{
            Class.forName("com.mysql.jdbc.Driver");
	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            PreparedStatement ps= con.prepareStatement("Select * from request where r_date=?");
            ps.setString(1,inputdate);
            ResultSet rs=ps.executeQuery();
            if (rs.next()) {
                new Viewrequestdetailframe(inputdate).setVisible(true);
                this.dispose(); 
            
            } 
            else {
                lblmessage.setText("No record found!");
            }
        }
        catch(Exception e){
              System.out.println(e);
        }
    }//GEN-LAST:event_btgoActionPerformed

