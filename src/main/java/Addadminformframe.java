        try{
            Random r1=new Random();
            int a=r1.nextInt(1,9);
            int b=r1.nextInt(1,9);
            int c=r1.nextInt(1,9);
            int d=r1.nextInt(1,9);
	    String adm_id= "ISCAD"+a+""+b+""+c+""+d;
            String adm_password= "rd"+c+""+d+""+"@@adm";
            String adm_name= textname.getText().trim();
            String acno=textcno.getText().trim();
            String desig = cmbdesig.getSelectedItem().toString();
            String salary = textsal.getText().trim();
            String qualify =textqualify .getText().trim();
            String j_date = textdate.getText().trim();
            if (adm_name.isEmpty() || acno.isEmpty() || salary.isEmpty() || qualify.isEmpty()|| j_date.isEmpty()) {
                lblmess.setText("❌ Please fill all the mandatory fields.");
                return;
            }
            if (!j_date.matches("\\d{4}-\\d{2}-\\d{2}")) {
                lblmess.setText("❌ Please enter date in YYYY-MM-DD format.");
                return;
            }
            Class.forName("com.mysql.jdbc.Driver");
	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            String qry="insert into admin (adm_id,adm_password,adm_name,acno,desig,salary,qualify,j_date) values(?,?,?,?,?,?,?,?)";
            PreparedStatement ps1= con.prepareStatement(qry);
	    ps1.setString(1,adm_id);
	    ps1.setString(2,adm_password);
	    ps1.setString(3,adm_name);
	    ps1.setString(4,acno);
	    ps1.setString(5,desig);
	    ps1.setString(6,salary);
	    ps1.setString(7,qualify);
	    ps1.setString(8,j_date);
            int a1=ps1.executeUpdate();
            if (a1==1){
                dispose();
                new Hireconfirmframe(adm_id,adm_password,"Admin").setVisible(true);
            }
            else{
                lblmess.setText("ERROR!! Try Again");
            }
        }
