    private void btshowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btshowActionPerformed
        String select= cmbgroup.getSelectedItem().toString();
        System.out.println(select);
        String qry="";
        switch (select){
            case "Service":
                qry="Select s_type as value, count(*) as total from request  group by s_type";
                break;
            case "Landmark":
                qry="Select l_mark as value, count(*)as total from request group by l_mark";
                break;
            case "Prefer Date":
                qry="Select p_date as value, count(*) as total from request group by p_date";
                break;            
            case "Preffer Time":
                qry="Select p_time as value, count(*) as total from request group by p_time";
                break;
            case "Request Date":
                qry="Select r_date as value, count(*) as total from request group by r_date";
                break;
            case "Appliance Type":
                qry="Select appl_type as value, count(*) as total from request where appl_type is not NULL group by appl_type";
                break;
            case "Brand":
                qry="Select brand as value, count(*) as total from request where brand is not NULL group by brand";
                break;
            case "Status":
                qry="Select status as value, count(*) as total from status group by status";
                break;
            default:
                lblmess.setText("Please Enter Valid Choice");
                ((DefaultTableModel)tbblresult.getModel()).setRowCount(0);
                return;       
        }
        try{
            tbblresult.setVisible(true);
            Class.forName("com.mysql.jdbc.Driver");
	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost/homesupport?user=root&password=satyam");
            Statement ps= con.createStatement();
            ResultSet rs = ps.executeQuery(qry); 
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Value");
            model.addColumn("Count");
            boolean found= false;
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("value"),rs.getInt("total")});
                found=true;
            }    
            tbblresult.setModel(model);
            lblmess.setText(found ? "":"No Record Found");
           
            
        }
        catch(Exception e){
            
             System.out.println(e);   
        }
    }//GEN-LAST:event_btshowActionPerformed

